class Product {
  final int id;
  final String name;
  final String description;
  final String category;
  final double price;
  final int quantity;
  final int sku;
  final String photoUrl;
  final bool inStock;

  Product(
    this.id,
    this.name,
    this.description,
    this.category,
    this.price,
    this.quantity,
    this.sku,
    this.photoUrl,
    this.inStock,
  );
}
